# math_operations.py

def add(a, b):
    """
    Returns the sum of two numbers.
    """
    return a + b

def subtract(a, b):
    """
    Returns the difference between two numbers.
    """
    return a - b

def multiply(a, b):
    """
    Returns the multiplication between two numbers.
    """
    return a * b

